diaSemana = int(input("digite um numero de 1 a 7:"))

if(diaSemana == 1):
    print("domingo")

elif(diaSemana == 2):
    print("segunda")

elif(diaSemana == 3):
    print("terça")

elif(diaSemana == 4):
    print("quarta")

elif(diaSemana == 5):
    print("quinta")

elif(diaSemana == 6):
    print("sexta")

elif(diaSemana == 7):
    print("saado")
else:
    print("não existe esse dia")